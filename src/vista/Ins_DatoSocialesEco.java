    public String formaTrabajo(){
        String tipo = "";
        if(Integer.parseInt(Jtf_Fijo.getText())> 0){
            tipo = "Fijo";
        }else if(Integer.parseInt(Jtf_Esporadico.getText())> 0){
            tipo = "Esporadico";
        }else if(Integer.parseInt(Jtf_Propio.getText())> 0){
            tipo = "Propio";
        }
        return tipo;
    }
    public int calculoingreso(int fijo, int esporadico, int propio){
        int total = fijo + esporadico + propio;
        this.Jtf_TotalIngresos.setText(Integer.toString(total));
        ingresos = total ;
        return total;
    }
