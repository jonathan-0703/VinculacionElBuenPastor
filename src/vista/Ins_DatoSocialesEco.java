    public void agregarDatosSocialesEco(){
        this.setForma_trabajo(formaTrabajo());
        this.setPer_sustento(this.JCB_sustento.getSelectedItem().toString());
        
        if (this.Jtf_OtrosIngresos.getText().equals("")){
            otros_ing = "Ninguno";
        }else{
            this.setOtros_ing(this.Jtf_OtrosIngresos.getText());
        }
        
        ingreso = calculoingreso(Integer.parseInt(Jtf_Fijo.getText()), Integer.parseInt(Jtf_Esporadico.getText()), Integer.parseInt(Jtf_Propio.getText()));
        //data_social.setPer_total_ingreso(calculoingreso(Integer.parseInt(Jtf_Fijo.getText()), Integer.parseInt(Jtf_Esporadico.getText()), Integer.parseInt(Jtf_Propio.getText()))); 
        this.setGast_mensual(this.Jtf_gastos.getText());
        Controlador ctrl = new Controlador();
        ctrl.cargaSocioEconomico(this.getForma_trabajo(), this.getPer_sustento(), this.getOtros_ing(),this.getGast_mensual());

        
        
    }

